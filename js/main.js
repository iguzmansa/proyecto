function initMap() {
  map = new google.maps.Map(document.getElementById("map"), {
    center: {lat: 9.981710, lng: -84.091415},  
    zoom: 19,
  });
  
  infoWindow = new google.maps.InfoWindow();

  const locationButton = document.createElement("button");

  locationButton.textContent = "Ver mi ubicacion";
  locationButton.classList.add("custom-map-control-button");
  map.controls[google.maps.ControlPosition.TOP_CENTER].push(locationButton);
  locationButton.addEventListener("click", () => {
    // Try HTML5 geolocation.
    if (navigator.geolocation) {
      navigator.geolocation.getCurrentPosition(
        (position) => {
          const pos = {
            lat: position.coords.latitude,
            lng: position.coords.longitude,
          };

          infoWindow.setPosition(pos);
          infoWindow.setContent("Ubicacion encontrada");
          infoWindow.open(map);
          map.setCenter(pos);
        },
        () => {
          handleLocationError(true, infoWindow, map.getCenter());
        },
      );
    } else {
      // Browser doesn't support Geolocation
      handleLocationError(false, infoWindow, map.getCenter());
    }
  });
}
//Manejo de Errores
function handleLocationError(browserHasGeolocation, infoWindow, pos) {
  infoWindow.setPosition(pos);
  infoWindow.setContent(
    browserHasGeolocation
      ? "Error: The Geolocation service failed."
      : "Error: Your browser doesn't support geolocation.",
  );
  infoWindow.open(map);
}

window.initMap = initMap;

document.getElementById("obtenerDireccion").addEventListener("click", solicitarPermisoGEO);

//Solicitar permisos de geolocalizaci√≥n para asignarlo a las variables globales
function solicitarPermisoGEO(){
    navigator.geolocation.getCurrentPosition(function(position)
    {
        origenLat = position.coords.latitude;
        OrigenLng = position.coords.longitude;

        //Utilizar latitud y longitud como punto de origen para la ruta
        origen = new google.maps.LatLng(origenLat, OrigenLng);

        trazarRuta(origen);
    });
}

function trazarRuta(origen){
    //Coordenas de destino
    let destino = new google.maps.LatLng(9.981710, -84.091415);

    let directionsService = new google.maps.DirectionsService();
    let directionsRenderer = new google.maps.DirectionsRenderer();

    directionsRenderer.setMap(map)

    var request = {
        origin: origen,
        destination: destino,
        travelMode: google.maps.DirectionsTravelMode.DRIVING,
    };

    directionsService.route(request, function(result, status)
    {
        if(status == google.maps.DirectionsStatus.OK){
            directionsRenderer.setDirections(result);
        }
    });

}


